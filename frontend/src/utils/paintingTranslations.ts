/**
 * Get Spanish translation for a painting title
 * Falls back to original title if no translation exists
 */
export function getSpanishTitle(englishTitle: string): string {
  return PAINTING_TRANSLATIONS_ES[englishTitle]?.title || englishTitle
}

/**
 * Get Spanish translation for a painting description
 * Falls back to original description if no translation exists
 */
export function getSpanishDescription(englishDescription: string, englishTitle: string): string {
  return PAINTING_TRANSLATIONS_ES[englishTitle]?.description || englishDescription
}

/**
 * Get localized painting data
 * Uses database fields if available, falls back to translation map
 */
export function getLocalizedPainting(
  painting: {
    title: string
    description: string
    titleEs?: string
    descriptionEs?: string
  },
  language: string
): { title: string; description: string } {
  if (language === 'es') {
    // First priority: Database Spanish fields
    if (painting.titleEs && painting.descriptionEs) {
      return {
        title: painting.titleEs,
        description: painting.descriptionEs
      }
    }
    
    // Second priority: Translation map (temporary fallback)
    const translatedTitle = getSpanishTitle(painting.title)
    const translatedDescription = getSpanishDescription(painting.description, painting.title)
    
    return {
      title: translatedTitle,
      description: translatedDescription
    }
  }
  
  // English - use original
  return {
    title: painting.title,
    description: painting.description
  }
}

